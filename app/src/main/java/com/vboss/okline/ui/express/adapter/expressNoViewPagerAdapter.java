public class expressNoViewPagerAdapter extends FragmentPagerAdapter {

    private List<Fragment> fragments;


    public expressNoViewPagerAdapter(FragmentManager fm, List<Fragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }

    @Override
    public int getCount() {
        return fragments.size();
    }
}
